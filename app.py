import streamlit as st
import pandas as pd

# –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
st.title("–ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫ –ø–∏—Ç–∞–Ω–∏—è –Ω–∞ –Ω–µ–¥–µ–ª—é")
st.write("–í—ã–±–µ—Ä–∏—Ç–µ –±–ª—é–¥–∞ –Ω–∞ –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ –¥–ª—è –æ–¥–Ω–æ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞.")

# –°–ø–∏—Å–æ–∫ –¥–Ω–µ–π –Ω–µ–¥–µ–ª–∏
days_of_week = ["–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫", "–í—Ç–æ—Ä–Ω–∏–∫", "–°—Ä–µ–¥–∞", "–ß–µ—Ç–≤–µ—Ä–≥", "–ü—è—Ç–Ω–∏—Ü–∞", "–°—É–±–±–æ—Ç–∞", "–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ"]

# –°–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –±–ª—é–¥
available_dishes = {
    "–°—É–ø—ã": ["–ë–æ—Ä—â", "–ö—É—Ä–∏–Ω—ã–π —Å—É–ø", "–ì—Ä–∏–±–Ω–æ–π —Å—É–ø"],
    "–í—Ç–æ—Ä—ã–µ –±–ª—é–¥–∞": ["–ö–æ—Ç–ª–µ—Ç—ã —Å –ø—é—Ä–µ", "–ü–ª–æ–≤", "–ú–∞–∫–∞—Ä–æ–Ω—ã —Å —Å—ã—Ä–æ–º"],
    "–°–∞–ª–∞—Ç—ã": ["–û–ª–∏–≤—å–µ", "–í–∏–Ω–µ–≥—Ä–µ—Ç", "–°–∞–ª–∞—Ç –¶–µ–∑–∞—Ä—å"]
}

# –°–ª–æ–≤–∞—Ä—å –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∑–∞–∫–∞–∑–æ–≤
orders = {}

# –°–æ–∑–¥–∞–µ–º —Å–µ–∫—Ü–∏–∏ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
for day in days_of_week:
    st.header(f"üçΩÔ∏è {day}")
    
    # –í–∏–¥–∂–µ—Ç—ã –¥–ª—è –≤—ã–±–æ—Ä–∞ –±–ª—é–¥ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    soup_choice = st.selectbox(f"–í—ã–±–µ—Ä–∏—Ç–µ —Å—É–ø –Ω–∞ {day.lower()}:", ["–ù–µ –≤—ã–±—Ä–∞–Ω–æ"] + available_dishes["–°—É–ø—ã"])
    main_course_choice = st.selectbox(f"–í—ã–±–µ—Ä–∏—Ç–µ –≤—Ç–æ—Ä–æ–µ –±–ª—é–¥–æ –Ω–∞ {day.lower()}:", ["–ù–µ –≤—ã–±—Ä–∞–Ω–æ"] + available_dishes["–í—Ç–æ—Ä—ã–µ –±–ª—é–¥–∞"])
    salad_choice = st.selectbox(f"–í—ã–±–µ—Ä–∏—Ç–µ —Å–∞–ª–∞—Ç –Ω–∞ {day.lower()}:", ["–ù–µ –≤—ã–±—Ä–∞–Ω–æ"] + available_dishes["–°–∞–ª–∞—Ç—ã"])
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±–æ—Ä –≤ —Å–ª–æ–≤–∞—Ä—å
    orders[day] = {
        "–°—É–ø": soup_choice,
        "–í—Ç–æ—Ä–æ–µ –±–ª—é–¥–æ": main_course_choice,
        "–°–∞–ª–∞—Ç": salad_choice
    }

# –ö–Ω–æ–ø–∫–∞ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Å–≤–æ–¥–∫–∏ –∑–∞–∫–∞–∑–∞
st.markdown("---")
st.header("üìÑ –í–∞—à–µ –º–µ–Ω—é –Ω–∞ –Ω–µ–¥–µ–ª—é")
if st.button("–ü–æ–∫–∞–∑–∞—Ç—å –º–æ–π –∑–∞–∫–∞–∑"):
    # –°–æ–∑–¥–∞–µ–º DataFrame –¥–ª—è —É–¥–æ–±–Ω–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
    df = pd.DataFrame(orders).transpose()
    df = df.replace("–ù–µ –≤—ã–±—Ä–∞–Ω–æ", "‚Äî")
    st.dataframe(df)

# –ö–Ω–æ–ø–∫–∞ –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ –ø—Ä–æ–¥—É–∫—Ç–æ–≤ (–∑–¥–µ—Å—å –±—É–¥–µ—Ç –≤–∞—à–∞ –ª–æ–≥–∏–∫–∞)
st.markdown("---")
if st.button("–ü–æ–¥—Å—á–∏—Ç–∞—Ç—å –ø—Ä–æ–¥—É–∫—Ç—ã"):
    st.warning("–§—É–Ω–∫—Ü–∏—è –ø–æ–¥—Å—á–µ—Ç–∞ –ø—Ä–æ–¥—É–∫—Ç–æ–≤ –ø–æ–∫–∞ –Ω–µ —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞.")
    st.write("–ó–¥–µ—Å—å –±—É–¥–µ—Ç –ª–æ–≥–∏–∫–∞, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–æ—Ö–æ–¥–∏—Ç –ø–æ –≤–∞—à–µ–º—É –∑–∞–∫–∞–∑—É –∏ —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Å–ø–∏—Å–æ–∫ –ø–æ–∫—É–ø–æ–∫.")
